#include <unistd.h>

void	draw_grid(char *tab)
{
	int	i;

	i = 0;
	while (tab[i] != '\0')
	{
		write(1, &tab[i], 1);
		if ((i + 1) % 4 == 0)
		{
			write(1, "\n", 1);
		}
		else
		{
			write(1, " ", 1);
		}
		i++;
	}
}
