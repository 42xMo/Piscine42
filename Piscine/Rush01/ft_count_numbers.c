int	ft_count_numbers(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (i % 2 == 0 && !(str[i] >= '1' && str[i] <= '4'))
			return (0);
		else if (i % 2 == 1 && str[i] != ' ')
			return (0);
		i++;
	}
	return (i);
}
